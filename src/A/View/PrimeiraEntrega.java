    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonListarActionPerformed
    {//GEN-HEADEREND:event_jButtonListarActionPerformed
        Aluno aluno = new Aluno();
        File arquivo = new File(jTextFieldArquivo.getText());
        ListarAlunoController alunoController = new ListarAlunoController();
        List<Aluno> alunos = alunoController.listar(arquivo);

        for(Aluno a : alunos)
        {
            tab.addRow(new String[] {
                a.getCodMunicipal(),
                a.getNome(),
                a.getMae(),
                a.getPai()});               
        }
        tab.fireTableDataChanged();
    }//GEN-LAST:event_jButtonListarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonExcluirActionPerformed
    {//GEN-HEADEREND:event_jButtonExcluirActionPerformed
        int linhaSelecionada = jTableAlunos.getSelectedRow();
        if(linhaSelecionada != -1)
        {
            tab.removeRow(linhaSelecionada);
            tab.fireTableDataChanged();
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonAtualizarActionPerformed
    {//GEN-HEADEREND:event_jButtonAtualizarActionPerformed
        
        int linhaSelecionada = jTableAlunos.getSelectedRow();
        if(linhaSelecionada != -1)
        {
            tab.setValueAt(jTextFieldCodMunicipal.getText(), linhaSelecionada, 0);
            tab.setValueAt(jTextFieldNomeAluno.getText(), linhaSelecionada, 1);
            tab.setValueAt(jTextFieldMae.getText(), linhaSelecionada, 2);
            tab.setValueAt(jTextFieldPai.getText(), linhaSelecionada, 3);
        }
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

