    private void jButtonSalvarAlterecoesActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonSalvarAlterecoesActionPerformed
    {//GEN-HEADEREND:event_jButtonSalvarAlterecoesActionPerformed
        File arquivo = new File(jTextFieldArquivo.getText());
        List<Aluno> alunos = new ArrayList<Aluno>();
        tab.fireTableDataChanged();
        
        for(int linha = 0; linha < tab.getRowCount(); linha++)
        {
            Aluno a = new Aluno();
            a.setCodMunicipal(tab.getValueAt(linha, 0).toString());
            a.setNome(tab.getValueAt(linha, 1).toString());
            a.setMae(tab.getValueAt(linha, 2).toString());
            a.setPai(tab.getValueAt(linha, 3).toString());
            alunos.add(a);           
        }
        CadastrarAlunoController controller = new CadastrarAlunoController();
        controller.cadastrar(arquivo, alunos);
    }//GEN-LAST:event_jButtonSalvarAlterecoesActionPerformed

    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonCadastrarActionPerformed
    {//GEN-HEADEREND:event_jButtonCadastrarActionPerformed
        Object[] row = new Object[4];
        row[0] = jTextFieldCodMunicipal.getText();
        row[1] = jTextFieldNomeAluno.getText();
        row[2] = jTextFieldMae.getText();
        row[3] = jTextFieldPai.getText();
        tab.addRow(row);
        tab.fireTableDataChanged();
        
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void jButtonLimparTabActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButtonLimparTabActionPerformed
    {//GEN-HEADEREND:event_jButtonLimparTabActionPerformed
        tab.fireTableDataChanged();
        for(int i = 0; i < tab.getRowCount(); i++)
        {
            tab.removeRow(i);
        }
        tab.fireTableDataChanged();
    }//GEN-LAST:event_jButtonLimparTabActionPerformed

